#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	size_t	dest_length;

	i = 0;
	dest_length = ft_strlen((const char *)dest);
	while (src[i] != '\0')
	{
		dest[dest_length + i] = src[i];
		i++;
	}
	dest[dest_length + i] = '\0';
	return (dest);
}
